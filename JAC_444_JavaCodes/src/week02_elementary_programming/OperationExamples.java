package week02_elementary_programming;

public class OperationExamples {
    public static void main(String[] args) {
        System.out.println("3 / 4 = " + 3 / 4); // 0.75 --> 0
        System.out.println("3.0 / 4 = " + 3.0 / 4);
        System.out.println("3 / 4.0 = " + 3 / 4.0);
        System.out.println("3.0 / 4.0 = " + 3.0 / 4.0);

        System.out.println("****************************");

        System.out.println("9 % 4 = " + 9 % 4);
        System.out.println("9.0 % 4 = " + 9.0 % 4);
        System.out.println("9 % 4.0 = " + 9 % 4.0);
        System.out.println("9.0 % 4.0 = " + 9.0 % 4.0);
    }
}
